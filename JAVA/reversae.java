import java.util.Scanner;

public class reversae {
    public static void main(String[] args) {
        var bm = new Scanner(System.in);
        int oc = bm.nextInt();
        for (int i = 10; i <= oc; i++) {
            if (i == oc) {
                System.out.println("good go on");
            } else {
                System.out.println();
            }

            System.out.println(oc);

        }

    }

}
