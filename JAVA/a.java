import java.util.Scanner;

public class a {
    public static void main(String[] args) {
        Scanner ac = new Scanner(System.in);
        int n = ac.nextInt();
        for (int i = n; i <= n; i++) {
            for (int j = 1; j <= n; j--) {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
